DROP TABLE IF EXISTS auftraege;
DROP TABLE IF EXISTS kunden;

CREATE TABLE kunden (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(160) NOT NULL,
    email VARCHAR(190) NOT NULL UNIQUE,
    ort VARCHAR(120) NOT NULL,
    erstellt_am TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE auftraege (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    kunde_id INTEGER NOT NULL REFERENCES kunden(id) ON DELETE CASCADE,
    titel VARCHAR(180) NOT NULL,
    netto NUMERIC(10,2) NOT NULL CHECK (netto >= 0),
    status VARCHAR(30) NOT NULL CHECK (status IN ('neu', 'in_bearbeitung', 'fertig', 'storniert')),
    erstellt_am TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);
